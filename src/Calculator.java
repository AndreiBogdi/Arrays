public class Calculator {
    public static double sum(double first, double second) {
        double adunare = first + second;
        return adunare;
    }

    public static double minus(double first, double second) {
        double scadere = first - second;
        return scadere;
    }

    public static int multiplication(int first, int second) {
        double inmutire = first * second;
        return (int) inmutire;
    }

    public static float division(float first, float second) {
        double divisionResult = first / second;
        return (float) divisionResult;
    }
}
